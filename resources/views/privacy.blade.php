<h1>Privacy Policy – WIKU</h1>
<p class="meta">
  Dikeluarkan oleh: <strong>Pemerintah Kabupaten Cilacap</strong><br>
  Terakhir diperbarui: <strong>4 Maret 2025</strong>
</p>

<p>
  Selamat datang di <strong>WIKU</strong> (selanjutnya disebut "Aplikasi"), aplikasi layanan digital pemerintahan yang dikembangkan oleh <strong>Pemerintah Kabupaten Cilacap</strong> untuk melayani masyarakat Kabupaten Cilacap, Jawa Tengah, Indonesia.
</p>
<p>
  Kebijakan Privasi ini menjelaskan bagaimana kami mengumpulkan, menggunakan, menyimpan, dan melindungi informasi pribadi Anda ketika Anda menggunakan Aplikasi ini.
</p>

<h2>1. Informasi yang Kami Kumpulkan</h2>
<p>Dalam rangka menyediakan layanan, Aplikasi dapat mengumpulkan informasi berikut:</p>
<ul>
  <li><strong>Identitas Pengguna:</strong> Nama lengkap, Nomor Induk Kependudukan (NIK), nomor telepon, dan alamat email.</li>
  <li><strong>Data Akun:</strong> Nomor telepon sebagai identitas login, token autentikasi (JWT), dan data sesi.</li>
  <li><strong>Data Perijinan:</strong> Informasi yang Anda masukkan saat mengajukan permohonan perizinan (KKPR, Siteplan, SPPM), termasuk dokumen, koordinat lokasi, dan data pemohon.</li>
  <li><strong>Data Pengaduan (LaporBup):</strong> Laporan aduan, foto, dan koordinat GPS lokasi kejadian.</li>
  <li><strong>Data Lokasi:</strong> Koordinat GPS perangkat Anda, hanya digunakan saat Anda mengajukan laporan atau menggunakan fitur peta.</li>
  <li><strong>Data Perangkat:</strong> Token Firebase Cloud Messaging (FCM) untuk keperluan pengiriman notifikasi push.</li>
  <li><strong>Data Aktivitas:</strong> Log penggunaan aplikasi untuk keperluan pemeliharaan dan peningkatan layanan.</li>
</ul>

<h2>2. Tujuan Penggunaan Data</h2>
<p>Data yang kami kumpulkan digunakan untuk tujuan-tujuan berikut:</p>
<ul>
  <li>Memproses permohonan perizinan dan layanan pemerintahan lainnya.</li>
  <li>Mengirimkan notifikasi terkait status permohonan atau pengaduan.</li>
  <li>Memverifikasi identitas pengguna dan mencegah penyalahgunaan layanan.</li>
  <li>Meningkatkan kualitas layanan digital Pemerintah Kabupaten Cilacap.</li>
  <li>Keperluan statistik dan pelaporan internal pemerintah (data dianonimkan).</li>
  <li>Memfasilitasi komunikasi darurat melalui fitur layanan darurat.</li>
</ul>

<h2>3. Dasar Hukum Pengolahan Data</h2>
<p>
  Pengolahan data pribadi dalam Aplikasi ini dilakukan berdasarkan:
</p>
<ul>
  <li>Undang-Undang Nomor 27 Tahun 2022 tentang Perlindungan Data Pribadi.</li>
  <li>Peraturan Pemerintah yang mengatur penyelenggaraan layanan pemerintah berbasis elektronik.</li>
  <li>Persetujuan eksplisit pengguna yang diberikan pada saat pendaftaran dan penggunaan layanan.</li>
</ul>

<h2>4. Berbagi Data dengan Pihak Ketiga</h2>
<p>
  Kami <strong>tidak menjual atau memperdagangkan</strong> data pribadi Anda kepada pihak mana pun. Data dapat dibagikan hanya dalam kondisi berikut:
</p>
<ul>
  <li><strong>Instansi Pemerintah Terkait:</strong> Data perizinan dapat dibagikan ke Organisasi Perangkat Daerah (OPD) Kabupaten Cilacap yang berwenang dalam memproses permohonan.</li>
  <li><strong>Penyedia Layanan Teknis:</strong> Kami menggunakan layanan pihak ketiga seperti Google Firebase untuk notifikasi push, Google Sign-In untuk autentikasi opsional, dan infrastruktur cloud untuk penyimpanan data. Penyedia ini terikat perjanjian kerahasiaan dan hanya memproses data sesuai instruksi kami.</li>
  <li><strong>Kewajiban Hukum:</strong> Apabila diwajibkan oleh peraturan perundang-undangan atau perintah pengadilan.</li>
</ul>

<h2>5. Izin Akses Perangkat</h2>
<p>Aplikasi meminta izin akses berikut pada perangkat Anda:</p>
<ul>
  <li><strong>Kamera:</strong> Untuk mengambil foto dokumen atau foto lokasi dalam pengajuan laporan.</li>
  <li><strong>Lokasi (GPS):</strong> Untuk menentukan koordinat saat mengajukan pengaduan atau menggunakan fitur peta wisata dan perijinan.</li>
  <li><strong>Penyimpanan:</strong> Untuk mengunggah dan menyimpan file dokumen perizinan.</li>
  <li><strong>Notifikasi:</strong> Untuk menerima pembaruan status perijinan dan pengaduan secara real-time.</li>
  <li><strong>Internet:</strong> Untuk mengakses layanan dan API pemerintah.</li>
</ul>
<p>Semua izin hanya diminta saat diperlukan dan dapat Anda cabut melalui pengaturan perangkat kapan saja.</p>

<h2>6. Penyimpanan dan Keamanan Data</h2>
<ul>
  <li>Data disimpan di server yang dikelola oleh Pemerintah Kabupaten Cilacap dan/atau penyedia cloud terpercaya.</li>
  <li>Transmisi data dilindungi menggunakan protokol enkripsi HTTPS/TLS.</li>
  <li>Autentikasi pengguna menggunakan JSON Web Token (JWT) dengan masa berlaku terbatas.</li>
  <li>Aplikasi mendukung Multi-Factor Authentication (MFA) menggunakan TOTP (Google Authenticator) untuk keamanan akun tambahan.</li>
  <li>Data sensitif tidak disimpan secara lokal di perangkat pengguna.</li>
</ul>

<h2>7. Retensi Data</h2>
<p>
  Data pribadi Anda disimpan selama akun Anda aktif dan selama diperlukan untuk memenuhi kewajiban hukum serta keperluan layanan pemerintahan. Anda dapat mengajukan penghapusan data dengan menghubungi kami melalui kontak yang tersedia di bawah.
</p>

<h2>8. Hak Pengguna</h2>
<p>Sebagai pengguna, Anda memiliki hak untuk:</p>
<ul>
  <li>Mengakses data pribadi yang kami simpan tentang Anda.</li>
  <li>Memperbarui atau mengoreksi data yang tidak akurat.</li>
  <li>Meminta penghapusan data akun Anda.</li>
  <li>Menarik persetujuan atas penggunaan data kapan saja.</li>
  <li>Mengajukan keberatan atas pengolahan data tertentu.</li>
</ul>

